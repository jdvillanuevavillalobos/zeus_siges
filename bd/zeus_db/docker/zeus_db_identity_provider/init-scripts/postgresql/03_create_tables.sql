-- üìå Crear funci√≥n para generar ULID x esquema
CREATE OR REPLACE FUNCTION idp_identity.generate_ulid()
RETURNS VARCHAR(26) AS $$
DECLARE
    timestamp_part TEXT;
    random_part TEXT;
    ulid TEXT;
BEGIN
    -- üïí Obtener el timestamp en milisegundos
    timestamp_part := lpad(to_hex((EXTRACT(EPOCH FROM clock_timestamp()) * 1000)::bigint), 12, '0');
    
    -- üé≤ Generar la parte aleatoria de 10 bytes y convertir a hex
    random_part := encode(gen_random_bytes(10), 'hex');

    -- üîÑ Concatenar timestamp + random para formar ULID
    ulid := upper(timestamp_part || random_part);

    -- üìè Asegurar que ULID tenga exactamente 26 caracteres (Base32 ULID usa 26)
    RETURN LEFT(ulid, 26);
END;
$$ LANGUAGE plpgsql;


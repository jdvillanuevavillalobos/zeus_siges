    updated_at TIMESTAMP, -- √öltima fecha de actualizaci√≥n
    updated_by VARCHAR(128), -- Usuario que realiz√≥ la √∫ltima modificaci√≥n
    deleted_at TIMESTAMP, -- Fecha de eliminaci√≥n del registro (soft delete)
    deleted_by VARCHAR(128), -- Usuario que elimin√≥ el registro
    transaction_id VARCHAR(64), -- ID de la transacci√≥n asociada (para trazabilidad)

    -- üìå Datos de la aplicaci√≥n
